public class Person{
// Take 4 fields
  //Create Getters and Setters
  // Implement Constructor argumented
  // Include private
  private String name;
  private int age;
   private int phno;
   private int id;

   public Person(String name,int age,int phno,int id){
    this.name = name;
    this.age = age;
    this.phno = phno;
    this.id = id;
   }

  public String getName(){
    return name;
  }
  public void setName(String name){
    this.name = name;
  }
  public int getAge(){
    return age; 
  }
  public void setAge(int age){
    this.age = age;
  }
  public int getPhno(){
    return phno;
  }
  public void setPhno(int phno){
    this.phno = phno;
  }
  public int getId(){
    return id;
  }
  public void setId(int id){
    this.id = id;
  }
    
  }

   

   
  

